import { writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { colors, fonts } from "../src/tokens.ts";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const OUTPUT_PATH = path.join(__dirname, "../src/tailwind.css");

function main() {
	console.time(" \x1b[32m✓\x1b[0m Generated tailwind.css in ");

	try {
		validateTokens();

		const nextCss = buildCss();
		writeFileSync(OUTPUT_PATH, nextCss, "utf-8");

		console.timeEnd(" \x1b[32m✓\x1b[0m Generated tailwind.css in ");
	} catch (error) {
		const message = error instanceof Error ? error.message : String(error);
		fail(message);
	}
}

function fail(message) {
	console.error(`\x1b[31m✗\x1b[0m ${message}`);
	process.exit(1);
}

function validateTokens() {
	for (const [fontName, fontValue] of Object.entries(fonts)) {
		if (typeof fontValue !== "string") {
			fail(`Invalid font token ${fontName}: expected string`);
		}
	}

	for (const [color, variants] of Object.entries(colors)) {
		for (const [variant, themes] of Object.entries(variants)) {
			if (!themes || typeof themes.light !== "string" || typeof themes.dark !== "string") {
				fail(`Invalid color token ${color}-${variant}: expected light and dark strings`);
			}
		}
	}
}

function buildCss() {
	const lines = [
		"/**",
		" * This file is automatically generated.",
		" * DO NOT EDIT manually",
		" */",
		"",
		"@theme inline {",
	];

	for (const [fontName, fontValue] of Object.entries(fonts)) {
		lines.push(`\t--font-${fontName}: ${fontValue};`);
	}

	for (const [color, variants] of Object.entries(colors)) {
		lines.push("");
		for (const variant of Object.keys(variants)) {
			lines.push(`\t--color-${color}-${variant}: var(--${color}-${variant});`);
		}
	}

	lines.push("}");
	lines.push("");
	lines.push("@media (prefers-reduced-motion: reduce) {");
	lines.push("\t*,");
	lines.push("\t*::before,");
	lines.push("\t*::after {");
	lines.push("\t\tanimation-duration: 0.01ms !important;");
	lines.push("\t\tanimation-iteration-count: 1 !important;");
	lines.push("\t\ttransition-duration: 0.01ms !important;");
	lines.push("\t}");
	lines.push("}");
	lines.push("");
	lines.push('@custom-variant dark (&:where([data-theme="dark"], [data-theme="dark"] *));');
	lines.push("");
	lines.push(":root {");

	for (const [color, variants] of Object.entries(colors)) {
		lines.push("");
		for (const variant of Object.keys(variants)) {
			lines.push(`\t--${color}-${variant}: ${variants[variant].light};`);
		}
	}

	lines.push("}");
	lines.push("");
	lines.push('[data-theme="dark"] {');

	for (const [color, variants] of Object.entries(colors)) {
		lines.push("");
		for (const variant of Object.keys(variants)) {
			lines.push(`\t--${color}-${variant}: ${variants[variant].dark};`);
		}
	}

	lines.push("}");

	return `${lines.join("\n")}\n`;
}

main();
